    \\/// Cell(T) â€” interior mutability for Copy T. M20f.
    \\///
    \\/// `set(self: *Self, value: T)` mutates through a mutable pointer.
    \\/// Bindings of type `Cell(T)` are emitted as Zig `var` so the
