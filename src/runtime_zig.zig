    \\//!
    \\//! Single-threaded `Rc<T>` / `Weak<T>` with explicit reference-count
    \\//! operations. V1 requires explicit `-rc` / `-w` to drop; automatic
    \\//! scope-exit drop is planned for M20e (before closure capture).
    \\
    \\const std = @import("std");
    \\
    \\/// Reference-counted box. `strong` counts owning handles; `weak`
    \\/// counts weak handles plus an implicit `+1` while `strong > 0` to
    \\/// keep the control block alive for weak-holders even after the
    \\/// value is logically gone.
    \\pub fn RcBox(comptime T: type) type {
    \\    return struct {
    \\        allocator: std.mem.Allocator,
    \\        strong: usize,
    \\        weak: usize,
    \\        value: T,
    \\
    \\        const Self = @This();
    \\
    \\        pub fn new(allocator: std.mem.Allocator, value: T) !*Self {
    \\            const box = try allocator.create(Self);
    \\            box.* = .{
    \\                .allocator = allocator,
    \\                .strong = 1,
    \\                .weak = 1, // implicit +1 while strong > 0
    \\                .value = value,
    \\            };
    \\            return box;
    \\        }
    \\
    \\        /// Increment strong count and return the same handle. Pairs
    \\        /// with an eventual `dropStrong`. Asserts the handle is alive.
    \\        pub fn cloneStrong(self: *Self) *Self {
    \\            std.debug.assert(self.strong > 0);
    \\            self.strong += 1;
    \\            return self;
    \\        }
    \\
    \\        /// Construct a weak handle. Increments the weak count.
    \\        /// Asserts a live strong count.
    \\        pub fn weakRef(self: *Self) WeakHandle(T) {
    \\            std.debug.assert(self.strong > 0);
    \\            self.weak += 1;
    \\            return .{ .ptr = self };
    \\        }
    \\
    \\        /// Decrement strong count. When it reaches zero, release the
    \\        /// implicit weak `+1`; if weak also reaches zero (no live weak
    \\        /// handles), free the box.
    \\        ///
    \\        /// V1: does NOT run a user-defined destructor on `value`. M20e
    \\        /// or later adds the Drop hook.
    \\        pub fn dropStrong(self: *Self) void {
    \\            std.debug.assert(self.strong > 0);
    \\            self.strong -= 1;
    \\            if (self.strong == 0) {
    \\                // Future: run user-defined Drop on `self.value` here.
    \\                self.weak -= 1; // release implicit +1
    \\                if (self.weak == 0) self.allocator.destroy(self);
    \\            }
    \\        }
    \\    };
    \\}
    \\
    \\/// Weak handle paired with `*T = RcBox(T)`. Holds an optional
    \\/// pointer; `null` after `dropWeak`. Does NOT keep `T` alive.
    \\pub fn WeakHandle(comptime T: type) type {
    \\    return struct {
    \\        ptr: ?*RcBox(T),
    \\
    \\        const Self = @This();
    \\
    \\        /// Increment weak count, return a new handle aliasing the
    \\        /// same control block. Safe when `ptr == null` (returns null).
    \\        pub fn cloneWeak(self: Self) Self {
    \\            if (self.ptr) |p| p.weak += 1;
    \\            return self;
    \\        }
    \\
    \\        /// Decrement weak count. When BOTH weak and strong reach
    \\        /// zero, free the box. Takes `Self` by value so weak
    \\        /// bindings can be emitted as `const` Zig (the original
    \\        /// design used `*Self` for defensive nulling, but Rig's
    \\        /// ownership pass already errors on `-w; -w` â€” see
    \\        /// `src/ownership.zig`'s `walkDrop`. Defensive nulling
    \\        /// without ownership integration would just paper over
    \\        /// bugs in the checker, and adding `var`-vs-`const` emit
    \\        /// rules for weak bindings adds complexity for no real
    \\        /// safety gain once the ownership invariant is upheld).
    \\        pub fn dropWeak(self: Self) void {
    \\            if (self.ptr) |p| {
    \\                std.debug.assert(p.weak > 0);
    \\                p.weak -= 1;
    \\                if (p.weak == 0 and p.strong == 0) p.allocator.destroy(p);
    \\            }
    \\        }
    \\
    \\        /// Attempt to obtain a fresh strong handle. Returns `null` if
    \\        /// the value has been dropped (`strong == 0`) or this weak
    \\        /// handle was nulled. On success, increments the strong count.
    \\        pub fn upgrade(self: Self) ?*RcBox(T) {
    \\            const p = self.ptr orelse return null;
    \\            if (p.strong == 0) return null;
    \\            p.strong += 1;
    \\            return p;
    \\        }
    \\    };
    \\}
